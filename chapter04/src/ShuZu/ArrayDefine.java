package ShuZu;

public class ArrayDefine {
    public static void main(String[] args) {
        /*
        数组的分类
        根据维度分：一维数组和二维数组
        一维数组[]：一组数据，类似于队列
        二维数组[][]：多组数据，类似与表格

        一维数组的声明/定义：
        语法格式：
        元素的数据类型[] 数组名;
         */

        // 如果要存储1组学员的成绩
        int[] scorse;

        // 如果要存储26个英文字母
        char[] letters;

        // 如果要存储一组学员的姓名
        String[] names;
    }
}
